	DATA R3, %KEYCODE-REGISTER
	LD R3, R3
	SHL R3
	SHL R3
	SHL R3
	DATA R0, 0xFF00
	LD R0, R0
	ADD R0, R3
	DATA R1, %ONE
	ADD R1, R0
	DATA R1, 0xFF00
	ST R1, R0
	LD R3, R0
	OUT Data, R2
	OUT Data, R0
	DATA R1, %LINE-WIDTH
	ADD R1, R2
	DATA R0, 0xFF00
	LD R0, R0
	DATA R1, 0x0007
	CMP R0, R1
	JMPE ROUTINE-io-drawFontCharacter-ENDLOOP
